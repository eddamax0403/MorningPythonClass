#While loop

#Increment
number = 105
while number <= 110 :
    print(number)
    number += 1

#Depreciation
num1 = 30
while num1 >= 25 :
    print(num1)
    num1 -= 1


#For loop
for count in range(1,10):
    print(count)

for x in range(20,30,3):
    print(x)

language = ["Python","C++","Java"]
for lang in language :
    print(lang)







